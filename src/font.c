static const font_glyph_t *find_glyph(char c)
{
    for (size_t i = 0; i < FONT_GLYPH_COUNT; i++) {
        if (lcd_font_8x16[i].ch == c) {
            return &lcd_font_8x16[i];
        }
    }
    return NULL;
}

static void render_glyph(int32_t x, int32_t y, char c, uint32_t on)
{
    const font_glyph_t *g = find_glyph(c);
    if (!g) {
        return;
    }

    for (int row = 0; row < FONT_H; row++) {
        int32_t py = y + row;
        if (py < 0 || py >= LCD_DISPLAY_HEIGHT) {
            continue;
        }

        uint8_t bits = g->rows[row];

        for (int col = 0; col < FONT_W; col++) {
            int32_t px = x + col;
            if (px < 0 || px >= LCD_DISPLAY_WIDTH) {
                continue;
            }

            if (bits & (0x80u >> col)) {
                set_lcd_pixel((uint32_t)px, (uint32_t)py, on);
            }
        }
    }
}

void render_text(int32_t x, int32_t y, const char *text, uint32_t on)
{
    if (!text) {
        return;
    }

    int32_t cursor_x = x;

    while (*text) {
        if (*text == ' ') {
            cursor_x += FONT_ADVANCE;
        } else {
            render_glyph(cursor_x, y, *text, on);
            cursor_x += FONT_ADVANCE;
        }
        text++;
    }
}